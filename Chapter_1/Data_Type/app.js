//primtive types

//String
const name = 'John Doe';
console.log(name);

//Number
const num = 10;
console.log(num);
console.log(typeof num);

//Boolean
const hasKids = true;
console.log(typeof hasKids);

//Null
const car = null;
console.log(typeof car)

//Undefine
let test;
console.log(typeof test);

//Symbol
const sym = Symbol();
console.log(typeof sym);

//-------------------------------

//Reference types

//Array
const arr = [4, 5, 6, 7, 7];
console.log(typeof arr);

//Object literal
const address = {
    city: 'boston',
    state: 'MA'
}
console.log(typeof address);

//Date
const today = new Date();
console.log(today);
console.log(typeof today);
